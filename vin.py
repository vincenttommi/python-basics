# type conversion in python
# you can convert one type from another using int()  float() and complex() methods.

x = 1
y = 3.4
z = 50


# converting int  to float
a = int(x) 
# converting from float to int

b = float(y)
# convert from it to complex
c = complex(z)



print(a)
print(b)
print(c)


print(type(a))
print(type(b))
print(type(c))

